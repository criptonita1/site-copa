/** Nome da seleção no idioma (fallback: o próprio PT). */
export function teamName(pt: string, lang: Lang): string {
  if (lang === "pt") return pt;
  return TEAM_NAMES[pt] ?? pt;
}

/** Nome da cidade no idioma (fallback: o próprio PT — muitas são iguais). */
export function cityName(pt: string, lang: Lang): string {
  if (lang === "pt") return pt;
  return CITY_NAMES[pt] ?? pt;
}

/** Interpola {chave} com params. */
export function interpolate(str: string, params?: Record<string, string | number>): string {
  if (!params) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) =>
    params[k] != null ? String(params[k]) : `{${k}}`,
  );
}
